# contoh for loops
buah = ['mangga', 'apel', 'alpukat']
for i in buah:
    print(i)

# for di dalam for
benda = ['meja', 'kursi', 'jam']
sayur = ['kangkung', 'tomat','kentang']
minuman = ['susu', 'kopi', 'eskrim']

daftar_belanja = [benda, sayur, minuman]
for subDaftarBelanja in daftar_belanja:
    print(subDaftarBelanja)

#contoh for in list
nama = ['agung', 'ridwan', 'budi']
for y in nama:
    print("namanya adalah ", y)

#contoh for string
huruf = ("python")
for text in huruf:
    print("cekidot" ,text)